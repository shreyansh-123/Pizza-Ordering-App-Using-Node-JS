const mongoose = require('mongoose');

const Menu = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    size: {
        type: String,
        required: true
    }
})

const data = mongoose.model('data', Menu);

module.exports = data;